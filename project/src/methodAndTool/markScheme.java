package methodAndTool;

public class markScheme {

    String markPoint;
    int Score;

    public markScheme(String markPoint, int Score) {
        this.markPoint = markPoint;
        this.Score = Score;
    }

    public String getMarkPoint() {
        return this.markPoint;
    }

    public int getScore() {
        return this.Score;
    }

}
